export interface Region {
  code: string; // ISO 3166-1 alpha-2
  zh: string;
  en: string;
}

/** Convert ISO alpha-2 code to flag emoji: "US" → 🇺🇸 */
export function getFlag(code: string): string {
  return [...code.toUpperCase()]
    .map((c) => String.fromCodePoint(0x1f1e6 + c.charCodeAt(0) - 65))
    .join("");
}

/** "🇺🇸 美国" or "🇺🇸 United States" */
export function getRegionLabel(code: string, locale: string): string {
  const region = REGION_MAP.get(code);
  if (!region) return code;
  const name = locale.startsWith("zh") ? region.zh : region.en;
  return `${getFlag(code)} ${name}`;
}

/** Name only, for sorting */
export function getRegionName(code: string, locale: string): string {
  const region = REGION_MAP.get(code);
  if (!region) return code;
  return locale.startsWith("zh") ? region.zh : region.en;
}

